                    <h4>Mapungubwe</h4>
					<div class="tabContent">
						<p>Mapungubwe was declared as National monument on 17/08/84. Mapungubwe as an archaeological site received prominence in the 1930's with the discovery of golden ornaments and artefacts.  Mapungubwe is situated approximately 2,5 km south east of the confluence of the Shashi- and Limpopo rivers, on the farm Greefswald.  Mapungubwe consist of Bushveld sandstone and is approximately 300 meters long, it has a flatcrown vertical wreathe of some 30 meters high. The hill itself has an elongated shape, with 80 metre high cliffs surrounding the greater part of its periphery.  Access to the top of Mapungubwe is not easy, and there are only seven known ascents, of which only one is reasonably safe to use in both directions.<br><br>

Gold that was found in a graveyard on top of the hill, where 33 burials were discovered, each of which had a variety of golden beads, bangles and other golden decorations entombed with it.  In view of the fact that there were no burials with gold discovered elsewhere, it is clear that an aristocracy was living on top of Mapungubwe Hill.  Further archaeological evidence confirms this, and changes that took place socially, politically and culturally started the processes that led to the beginning of the system of divine kingship evident at Great Zimbabwe.<br><br>

Settlement structure at Mapungubwe places the hill at the centre, with the town spread out, around it.  There was clearly a demarcated main entrance for visitors to enter, which led them past houses to the foot of the hill where the court was situated between large upright boulders.  Excavations have uncovered steps, terrace walls and directional walls which clearly demarcated this area from the rest of the town.  It is generally accepted that the main way up to the top of the hill was through the court, and that there were several guards whose duty it was to limit access to the top of the hill, thus ensuring that the king was not disturbed.  We can be certain that the ruler lived up here, as it is not accepted in such a manner that commoners would look down upon his residence.  He also had to reside close to the court.  Evidence of the existence of the leader is non-existent and of other early villages, this is indicative of the fact that the ruler now has other priorities to which he must attend and can no longer be bothered by ordinary problems. Unless his advisors and councillors could not solve the problems, people wishing to see him would first have to get past all these officials before being granted and audience.  Numerous archaeological sites of these old villages are still to be seen along the Soutpansberg, extending deep into the heart of present-day Venda.  At about 1250 AD it appears as if the town of Mapungubwe was abandoned for reasons still unknown.<br><br>

The importance of Mapungubwe lies in the first manifestation in Southern Africa of a class-based society as opposed to a rank-based one prevalent earlier, and this led ultimately to the rise of the Great Zimbabwe Empire.
