                    <h4>MAKAPANS VALLEY HERITAGE SITE</h4>
                    <div class="tabContent">
1.	MAKAPANS VALLEY HERITAGE SITE

The Makapans Valley heritage sites are situated on the farm “Makapansgat” some 19-km east north east of Mokopane in Limpopo. The Makapans Valley is unique in that one kind find preserved in its many caves, in the form of sediments, fossils and archaeological remains, a record of human occupation and resource exploitation extending from australopithecine (ape-man) period about 3.32 million years before present through to present. This evidence forms the foundation of the South African prehistoric sequence.  Nowhere else within the confines of a single valley can such an extended and complete record of hominid activity be observed.  The most important sites in the valley are as follows:<br><br>

<strong>• The Makapansgat Lime works and adjacent "Cave of the Horse's Mandible"</strong>
	This is the oldest of the sites, spanning the time range of about 3.3.2 BP to about 1,6 BP. This site has yielded hundreds of thousands of fossil bones amongst which were found the remains of the gracile (lightly-built, smaller) australopithecine (Australopithecus africanus)<br><br>

<strong>• The Cave of hearths and adjacent "hyaena Cave"</strong>
	Cave of Hearths preserves the stone tools and other debris left by early man who first occupied the site at around 250 000 BP.  The overlying beds preserve an intermittent but extremely long record of human occupation, from +- 100 000 - 50 000 BP and again from 10 000 - 5 000 BP, and from iron age times to the present century.  It appears that between 200 000 BP and 100 000 BP the COH remained unoccupied.
	Fossils from the adjacent "Hyaena Cave " where the sediment infill is of greater age, suggest affinities with the Limeworks via occurrence of the remains of similar primitive hyaenas at both sites.<br><br>

<strong>• The Historic Cave of Makapansgat</strong>

	The Historic Cave lies immediately adjacent to the COH and preserves Iron Age and Mfecane relics, but is most famous as the scene of a clash between a Boer Commando and local Langa and Ndebele people.  Chief Makapan (Mokopane), together with a large number of his people and their livestock were besieged in the cave for nearly a month between 25 October and 18 November 1854 by Andries Potgieter.<br><br>

<strong>• The Rainbow Cave</strong>

	This lies immediately below the eastern chamber of the Historic Cave and contains the remains of several hearth, distinguished as different coloured ash and sediment horizons and hence the name of this little "cave".  The Rainbow Cave in all probability represents the lower part of the infill of the eastern chamber of the Historic Cave.  The exposed sediments have yielded Middle Stone Age artefacts of the Polokwane culture, in the age range +- 100 000 - 50 000 BP.
	The COH and Hyaena Cave, Historic Cave and Rainbow Cave together from part of a single interconnected large cavern complex, different portions of which were open to the surface and available for occupation at different times.<br><br>

<strong>• Peppercorn's Cave</strong>

	The cave contains Iron Age and recent relics, and an underground lake, which Mr Peppercorn utilizes as a source of water.  It is also home to a large colony of migratory long-fingered bats.  (Miniopteris schreibersii)<br><br>

<strong>• Ficus Cave and the Ficus Iron Age Site</strong>

	The cave contains Iron Age and 19th Century relics, a large bat colony and an underground lake of fluctuating depth.  An Iron Age site in the saddle between the Ficus Cave ridge and a small isolated hillock directly opposite the entrance, preserves occupational debris dating from +- 550 AD (early Iron age), ± 870 AD, and 1560 AD. As well as relics of Late Iron Age inhabitants who used the site immediately prior to white settler occupation.  The slopes adjacent to the saddle are artificially terraced containing archaeological finds.<br><br>

<strong> • Buffalo Cave</strong>

Dr R Broom took a small collection of fossils from the breccia of this remnant cave n 1937.  The breccia has yielded the remains of an extinct fossil buffalo, but the cave has been little studied and its contents remain undated.


